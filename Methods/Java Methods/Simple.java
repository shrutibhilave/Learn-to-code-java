public class Simple { //We will discuss a Simple Java Method in this File.

  public static void print() { //This is a Java method,Which will print "Hello World" on the cmd prompt.
    //Note : Keywords used : -
    //public : for the public access of method anywhere in this file or Another file.
    //static : It is used for a constant variable or a method that is same for every instance of a class.
    //void : returns nothing.
    System.out.println("Hello World");
  }

  public static void main(String[] args) {
    print();
  }
}
