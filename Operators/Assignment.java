public class Assignment {/*The Assignment Operators are used 
    for Assigning values to the variables*/

  public static void main(String[] args) {
    int x = 20; //This operator will simply assign 2 to the variable "x".
    System.out.println(x);
    x += 3; //This will add 3 to the variable "x"(It is also relates with increment operator).
    System.out.println(x);
    x -= 1; //This will subtract 1 from the variable "x"(It is also relates with decrement operator).
    System.out.println(x);
    x *= 2; //This will multiply the value of "x" by 2 and assign it to the variable of "x".
    System.out.println(x);
    x /= 3; //This will divide the value of "x" by 3 and assign it to the variable "x".
    System.out.println(x);
    x %= 2; //This will solve for the modulus of "x" and assign the answer to the variable "x".
    System.out.println(x);
    //Note : Any other value/Variable can be also used instead of the above given values.
  }
}
